- (void)viewDidLoad
{
    [super viewDidLoad];

    arrayCoin = [[NSMutableArray alloc ]init];
    arrayCoin = [coinControl getArrayLabelCoin];
    
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
